int main() {
    int x = 5;
    int y = 7;
    int sum = x + y;
    int diff = x - y;
    int prod = x * y;
    int quot = x / y;
    int rem = x % y;
    printf("The sum is: %d\n", sum);
    printf("The difference is: %d\n", diff);
    printf("The product is: %d\n", prod);
    printf("The quotient is: %d\n", quot);
    printf("The remainder is: %d\n", rem);
    return 0;
}
